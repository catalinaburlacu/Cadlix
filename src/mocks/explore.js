export function searchMovies(query) {
  if (!query || query.trim().length === 0) return []
  const q = query.toLowerCase().trim()
  return MOVIE_DATABASE.filter(
    movie =>
      movie.title.toLowerCase().includes(q) ||
      movie.type.toLowerCase().includes(q)
  ).slice(0, 10)
}

